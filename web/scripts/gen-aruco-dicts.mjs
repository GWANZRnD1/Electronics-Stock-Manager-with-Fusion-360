/**
 * Regenerate src/lib/aruco/dictionaries.ts from OpenCV's canonical ArUco data.
 * Run: node scripts/gen-aruco-dicts.mjs
 *
 * The DICT_NxN_M dictionaries are prefixes of the corresponding _1000 list, so we
 * take the first M markers and keep only rotation 0 (the canonical orientation).
 * Bytes are decoded MSB-first row-major, matching OpenCV's getBitsFromByteList.
 */
import { writeFileSync } from "node:fs";

const URL_HPP =
  "https://raw.githubusercontent.com/opencv/opencv/4.x/modules/objdetect/src/aruco/predefined_dictionaries.hpp";

const TARGETS = [
  { sym: "DICT_4X4_1000_BYTES", grid: 4, nBytes: 2, take: 50, name: "4X4_50" },
  { sym: "DICT_5X5_1000_BYTES", grid: 5, nBytes: 4, take: 100, name: "5X5_100" },
  { sym: "DICT_6X6_1000_BYTES", grid: 6, nBytes: 5, take: 250, name: "6X6_250" },
];

const src = await (await fetch(URL_HPP)).text();

/** Every integer inside one array's initializer (no semicolons appear inside it). */
function flatBytes(sym) {
  const at = src.indexOf(sym);
  if (at < 0) throw new Error(`symbol not found: ${sym}`);
  const eq = src.indexOf("=", at);
  const end = src.indexOf(";", eq);
  return [...src.slice(eq + 1, end).matchAll(/\d+/g)].map((m) => Number(m[0]));
}

let ts = `// AUTO-GENERATED by scripts/gen-aruco-dicts.mjs from OpenCV predefined_dictionaries.hpp (4.x).\n`;
ts += `// Do not edit by hand. Each marker is the canonical (rotation-0) byte packing of its\n`;
ts += `// grid*grid bits, MSB-first row-major. Decode with bitsFromBytes() in marker.ts.\n\n`;
ts += `export type ArucoDictName = "4X4_50" | "5X5_100" | "6X6_250";\n\n`;
ts += `export interface ArucoDict {\n  grid: number; // data cells per side (excludes the 1-cell black border)\n  markers: number[][]; // markers[id] = rotation-0 bytes\n}\n\n`;
ts += `export const ARUCO_DICTS: Record<ArucoDictName, ArucoDict> = {\n`;
for (const t of TARGETS) {
  const flat = flatBytes(t.sym);
  const perMarker = 4 * t.nBytes;
  ts += `  "${t.name}": {\n    grid: ${t.grid},\n    markers: [\n`;
  for (let i = 0; i < t.take; i++) {
    const base = i * perMarker;
    ts += `      [${flat.slice(base, base + t.nBytes).join(", ")}],\n`; // rotation 0 only
  }
  ts += `    ],\n  },\n`;
}
ts += `};\n`;

writeFileSync(new URL("../src/lib/aruco/dictionaries.ts", import.meta.url), ts);
console.log("wrote src/lib/aruco/dictionaries.ts");
